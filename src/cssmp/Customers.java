    private void get_DetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_get_DetailsActionPerformed
        // TODO add your handling code here:
        String getDits="SELECT * FROM CUSTOMER WHERE Cust_ID='"+cust_id.getText()+"'";
        ResultSet rs=SQL_Ops.output(getDits);
        try{
            while(rs.next()){
                name.setText(rs.getString(2));
                email.setText(rs.getString(3));
                phone.setText(Integer.toString(rs.getInt(4)));
                address.setText(rs.getString(5));
            }
        }
        catch(SQLException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error");
        }

    }//GEN-LAST:event_get_DetailsActionPerformed

