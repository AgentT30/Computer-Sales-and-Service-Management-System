    private void ListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListActionPerformed
        // TODO add your handling code here:
        if(Cust_Table.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "You have no customers!");
        }
        if(sort_Box.getSelectedItem().equals("default")){
            String output="SELECT * FROM CUSTOMER WHERE Cust_ID in (SELECT Cust_ID from SERVICES WHERE Emp_ID='"+emp_ID+"')";
            SQL_Ops.outputtoTableCust(SQL_Ops.output(output),Cust_Table,this);
        }
        else if(sort_Box.getSelectedItem().equals("Name")){
            String output="SELECT * FROM CUSTOMER WHERE Cust_ID in (SELECT Cust_ID from SERVICES WHERE Emp_ID='"+emp_ID+"')order by Cust_Name";
            SQL_Ops.outputtoTableCust(SQL_Ops.output(output),Cust_Table,this);
        }
        else if(sort_Box.getSelectedItem().equals("Email")){
            String output="SELECT * FROM CUSTOMER WHERE Cust_ID in (SELECT Cust_ID from SERVICES WHERE Emp_ID='"+emp_ID+"')order by Cust_Email";
            SQL_Ops.outputtoTableCust(SQL_Ops.output(output),Cust_Table,this);
        }
        else if(sort_Box.getSelectedItem().equals("Phone")){
            String output="SELECT * FROM CUSTOMER WHERE Cust_ID in (SELECT Cust_ID from SERVICES WHERE Emp_ID='"+emp_ID+"')order by Cust_Phone";
            SQL_Ops.outputtoTableCust(SQL_Ops.output(output),Cust_Table,this);
        }
        else{
            String output="SELECT * FROM CUSTOMER WHERE Cust_ID in (SELECT Cust_ID from SERVICES WHERE Emp_ID='"+emp_ID+"')order by Address";
            SQL_Ops.outputtoTableCust(SQL_Ops.output(output),Cust_Table,this);
        }

    }//GEN-LAST:event_ListActionPerformed

